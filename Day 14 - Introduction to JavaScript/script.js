var a = 20;
// post increement
var b = a++;
var c = ++a;
console.log(a); //22
console.log(b); //20
console.log(c) //22

// The increment operator (++) adds 1 from the operand.

// If it is placed after the operand, it returns the value before the increment.

// If it is placed before the operand, it returns the value after the increment.
